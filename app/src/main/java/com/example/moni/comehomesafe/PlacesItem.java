package com.example.moni.comehomesafe;


public class PlacesItem {

    private String name;
    private String place;

    public PlacesItem(String name, String place) {
        this.name = name;
        this.place = place;
    }

    public String getName() {
        return name;
    }

    public String getPlace() {
        return place;
    }

}
